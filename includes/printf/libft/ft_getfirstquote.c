#include "libft.h"

char	ft_getfirstquote(char *s)
{
	int	i;

	i = -1;
	if (!s)
		return (0);
	while (s[++i])
		if (s[i] == '"' || s[i] == '\'')
			return (s[i]);
	return (0);
}
