#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	size_t	i;
	size_t	len;

	if (!s1 || !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	res = (char *)malloc(sizeof(char) * (len + 1));
	if (!res)
		return (NULL);
	*res = 0;
	i = -1;
	while (*s1)
		res[++i] = *s1++;
	while (*s2)
		res[++i] = *s2++;
	res[++i] = 0;
	return (res);
}
